const state = $state({ locale: 'pt-br' })

export function setLocale(locale) {
  state.locale = MESSAGES[locale] ? locale : 'pt-br'
}

export function getLocale() {
  return state.locale
}

export function has(key) {
  return key in MESSAGES[state.locale] || key in MESSAGES['pt-br']
}

export function t(key, params) {
  const s = MESSAGES[state.locale][key] ?? MESSAGES['pt-br'][key] ?? key
  if (!params) return s
  return s.replace(/\{(\w+)\}/g, (m, k) => (params[k] !== undefined ? String(params[k]) : m))
}

// Backend error details arrive as stable keys; translate known ones, pass raw text through.
export function apiError(detail) {
  if (detail == null) return t('game.err_generic_fallback')
  if (typeof detail === 'string') {
    if (has(`api.${detail}`)) return t(`api.${detail}`)
    const i = detail.indexOf(': ')
    if (i > 0 && has(`api.${detail.slice(0, i)}`)) {
      return `${t(`api.${detail.slice(0, i)}`)}: ${detail.slice(i + 2)}`
    }
    return detail
  }
  if (Array.isArray(detail?.errors)) return detail.errors.map(String).join(' · ')
  try {
    return JSON.stringify(detail)
  } catch {
    return String(detail)
  }
}

// Number formatting locale for toLocaleString.
export function numberLocale() {
  return state.locale === 'en' ? 'en-US' : 'pt-BR'
}
